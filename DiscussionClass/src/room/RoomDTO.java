package room;

public class RoomDTO {

		String roomUserID;
		int roomGroupNumber;
		int roomID;
		String roomTitle;
		String roomContent;
		String roomDate;
		String roomFile;
		String roomRealFile;
		int progress;
		
		public String getRoomUserID() {
			return roomUserID;
		}
		public void setRoomUserID(String roomUserID) {
			this.roomUserID = roomUserID;
		}
		public int getRoomGroupNumber() {
			return roomGroupNumber;
		}
		public void setRoomGroupNumber(int roomGroupNumber) {
			this.roomGroupNumber = roomGroupNumber;
		}
		public int getRoomID() {
			return roomID;
		}
		public void setRoomID(int roomID) {
			this.roomID = roomID;
		}
		public String getRoomTitle() {
			return roomTitle;
		}
		public void setRoomTitle(String roomTitle) {
			this.roomTitle = roomTitle;
		}
		public String getRoomContent() {
			return roomContent;
		}
		public void setRoomContent(String roomContent) {
			this.roomContent = roomContent;
		}
		public String getRoomDate() {
			return roomDate;
		}
		public void setRoomDate(String roomDate) {
			this.roomDate = roomDate;
		}
		public String getRoomFile() {
			return roomFile;
		}
		public void setRoomFile(String roomFile) {
			this.roomFile = roomFile;
		}
		public String getRoomRealFile() {
			return roomRealFile;
		}
		public void setRoomRealFile(String roomRealFile) {
			this.roomRealFile = roomRealFile;
		}
		public int getProgress() {
			return progress;
		}
		public void setProgress(int progress) {
			this.progress = progress;
		}
		
}
